package t1_beginner_problems.basic_strings;

public class PalindromeCheck {
    public boolean palindromeCheck(String s) {
        int l = 0, r = s.length() - 1;
        while (l < r) {
            if (s.charAt(l) != s.charAt(r))
                return false;
            l++;
            r--;
        }
        return true;
    }
}
